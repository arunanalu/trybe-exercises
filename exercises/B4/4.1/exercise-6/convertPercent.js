let a = 20;
if (a >= 0 && a <50) {
  a = 'F';
  console.log(a);
}
else if (a >= 50 && a < 60) {
  a = 'E';
  console.log(a);
}
else if (a >= 60 && a < 70)  {
  a = 'D';
  console.log(a);
}
else if (a >= 70 && a < 80) {
  a = 'C';
  console.log(a);
}
else if (a >= 80 && a < 90) {
  a = 'B';
  console.log(a);
}
else if (a >= 90 && a <= 100) {
  a = 'A';
  console.log(a);
}
else {
  console.log('Erro');
}